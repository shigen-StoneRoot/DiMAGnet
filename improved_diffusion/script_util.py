def _get_gd(distill: bool):
    """
    Dynamically choose gaussian diffusion module:
      - distill=True  -> gaussian_diffusion_distill
      - distill=False -> gaussian_diffusion
    """
    mod = ".gaussian_diffusion_distill" if distill else ".gaussian_diffusion"
    return importlib.import_module(mod, package=__package__)


